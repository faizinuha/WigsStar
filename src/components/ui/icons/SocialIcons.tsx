// Get icon by platform
export const getSocialIcon = (platform: string, className?: string) => {
  const iconProps = { className: className || 'h-4 w-4' };
  
  switch (platform.toLowerCase()) {
    case 'instagram':
      return <InstagramIcon {...iconProps} />;
    case 'twitter':
      return <TwitterIcon {...iconProps} />;
    case 'facebook':
      return <FacebookIcon {...iconProps} />;
    case 'tiktok':
      return <TikTokIcon {...iconProps} />;
    case 'youtube':
      return <Youtube {...iconProps} />;
    case 'linkedin':
      return <Linkedin {...iconProps} />;
    case 'github':
      return <Github {...iconProps} />;
    case 'discord':
      return <DiscordIcon {...iconProps} />;
    case 'spotify':
      return <SpotifyIcon {...iconProps} />;
    case 'twitch':
      return <TwitchIcon {...iconProps} />;
    case 'website':
      return <Globe {...iconProps} />;
    default:
      return <Globe {...iconProps} />;
  }
};

// Get platform color
export const getSocialColor = (platform: string): string => {
  switch (platform.toLowerCase()) {
    case 'instagram':
      return 'text-pink-500';
    case 'twitter':
      return 'text-foreground';
    case 'facebook':
      return 'text-blue-600';
    case 'tiktok':
      return 'text-foreground';
    case 'youtube':
      return 'text-red-500';
    case 'linkedin':
      return 'text-blue-500';
    case 'github':
      return 'text-foreground';
    case 'discord':
      return 'text-indigo-500';
    case 'spotify':
      return 'text-green-500';
    case 'twitch':
      return 'text-purple-500';
    default:
      return 'text-muted-foreground';
  }
};
